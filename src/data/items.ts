// Utility functions để tra cứu vật phẩm
export const getItemName = (id: number): string => {
  return ITEMS_DATA[id] || `Vật phẩm không xác định (ID: ${id})`;
};

export const getItemId = (name: string): number | null => {
  const entry = Object.entries(ITEMS_DATA).find(([, itemName]) => 
    itemName.toLowerCase() === name.toLowerCase()
  );
  return entry ? parseInt(entry[0]) : null;
};

export const searchItems = (query: string): Array<{ id: number; name: string }> => {
  const lowercaseQuery = query.toLowerCase();
  return Object.entries(ITEMS_DATA)
    .filter(([, name]) => name.toLowerCase().includes(lowercaseQuery))
    .map(([id, name]) => ({ id: parseInt(id), name }));
};

export const getAllItems = (): Array<{ id: number; name: string }> => {
  return Object.entries(ITEMS_DATA).map(([id, name]) => ({ 
    id: parseInt(id), 
    name 
  }));
};
